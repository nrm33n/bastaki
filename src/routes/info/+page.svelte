	<h1 class="font-display text-deep-olive">About Bastaki</h1>

	<h2>What is Bastaki?</h2>
	<p>
		<strong>Bastaki</strong> (also called Ajami, Achomi, Khodmooni, Lari, or Larestani) is an
		Iranian language spoken primarily in Larestan county in Fars Province, southern Iran, and
		by diaspora communities across the Persian Gulf states. UNESCO classifies it as
		<em>definitely endangered</em>.
	</p>

	<h2>Names and classification</h2>
	<p>
		The dialect goes by many names, reflecting the communities who speak it. <em>Bastaki</em>
		refers specifically to speakers with roots in Bastak. <em>Ajami</em> or <em>Achomi</em>
		is widely used among Gulf diaspora communities. The broader linguistic family
		(Larestani) includes several closely related varieties.
	</p>

	<h2>Basic grammar</h2>
	<p>
		Bastaki follows a subject–object–verb (SOV) order, typical of Iranian languages.
		A notable feature is its <strong>ergative</strong> alignment in past tenses: in past
		transitive clauses, the agent is marked differently from the patient, unlike in
		Modern Persian.
	</p>

